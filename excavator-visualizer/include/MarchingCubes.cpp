  geometry_msgs::msg::Point AddPoints(geometry_msgs::msg::Point* a, geometry_msgs::msg::Point* b) {
    geometry_msgs::msg::Point c;
    c.x = a->x + b->x;
    c.y = a->y + b->y;
    c.z = a->z + b->z;
    return c;
  }

  void SetVertex(const uint32_t i, const uint32_t j, const uint32_t k, const bool value) { m_Vertices.at(i).at(j).at(k) = value; }

  void SetVertices(const uint32_t i, const uint32_t j, const uint32_t length) {
    std::fill(m_Vertices.at(i).at(j).begin(), m_Vertices.at(i).at(j).end(), 0);
    std::fill(m_Vertices.at(i).at(j).begin(), m_Vertices.at(i).at(j).begin() + length, 1);
  }

  void RebuildCubes() {
    for (uint32_t i = 0; i < m_Step.x; ++i)
      for (uint32_t j = 0; j < m_Step.y; ++j)
        for (uint32_t k = 0; k < m_Step.z; ++k) {
          // if (!m_UpdateFlag[i][j][k]) continue;
          // m_UpdateFlag[i][j][k] = 0;
          m_Cubes.at(i).at(j).at(k) = m_Vertices.at(i + 0).at(j + 0).at(k + 0);
          m_Cubes.at(i).at(j).at(k) |= m_Vertices.at(i + 1).at(j + 0).at(k + 0) << 1;
          m_Cubes.at(i).at(j).at(k) |= m_Vertices.at(i + 1).at(j + 1).at(k + 0) << 2;
          m_Cubes.at(i).at(j).at(k) |= m_Vertices.at(i + 0).at(j + 1).at(k + 0) << 3;
          m_Cubes.at(i).at(j).at(k) |= m_Vertices.at(i + 0).at(j + 0).at(k + 1) << 4;
          m_Cubes.at(i).at(j).at(k) |= m_Vertices.at(i + 1).at(j + 0).at(k + 1) << 5;
          m_Cubes.at(i).at(j).at(k) |= m_Vertices.at(i + 1).at(j + 1).at(k + 1) << 6;
          m_Cubes.at(i).at(j).at(k) |= m_Vertices.at(i + 0).at(j + 1).at(k + 1) << 7;
        }
  }

  void CreateMessage(visualization_msgs::msg::Marker* msg) {
    msg->points.clear();
    geometry_msgs::msg::Point orig;
    geometry_msgs::msg::Point vertex;

    for (uint32_t i = 0; i < m_Step.x; ++i)
      for (uint32_t j = 0; j < m_Step.y; ++j)
        for (uint32_t k = 0; k < m_Step.z; ++k) {
          orig.x = m_Orig.x + i * m_Size.x;
          orig.y = m_Orig.y + j * m_Size.y;
          orig.z = m_Orig.z + k * m_Size.z;
          const uint8_t cube = m_Cubes.at(i).at(j).at(k);
          for (int8_t idx = 15; idx > -1; --idx) {
            auto l = m_Triangles[cube][idx];
            if (l != -1) {
              vertex = m_Nodes[l];
              msg->points.push_back(AddPoints(&orig, &vertex));
            }
          }
        }
  }

  double CalculateVolume() {
    double volume = 0;
    for (auto& r1 : m_Cubes)
      for (auto& r2 : r1)
        for (auto& r3 : r2) volume += m_Volumes[r3];

    return volume * this->m_Size.x * this->m_Size.y * this->m_Size.z;
  }

  bool sideSign(geometry_msgs::msg::Point32* A, geometry_msgs::msg::Point32* B, geometry_msgs::msg::Point32* C, geometry_msgs::msg::Point32* P) {
    geometry_msgs::msg::Point32 B_;
    geometry_msgs::msg::Point32 C_;
    geometry_msgs::msg::Point32 P_;
    B_.x = B->x - A->x;
    B_.y = B->y - A->y;
    B_.z = B->z - A->z;

    C_.x = C->x - A->x;
    C_.y = C->y - A->y;
    C_.z = C->z - A->z;

    P_.x = P->x - A->x;
    P_.y = P->y - A->y;
    P_.z = P->z - A->z;
    // Bx*Cy*Pz - Bx*Cz*Py - By*Cx*Pz + By*Cz*Px + Bz*Cx*Py - Bz*Cy*Px
    double det = +B_.x * C_.y * P_.z;
    det += -B_.x * C_.z * P_.y;
    det += -B_.y * C_.x * P_.z;
    det += +B_.y * C_.z * P_.x;
    det += +B_.z * C_.x * P_.y;
    det += -B_.z * C_.y * P_.x;

    return (det > 0);
  }

